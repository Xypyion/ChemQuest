/* ----------------------------- core helpers ----------------------------- */

function getLang() {
  const l = localStorage.getItem('cq_lang');
  return l === 'th' ? 'th' : 'en';
}

function setLang(l) {
  localStorage.setItem('cq_lang', l === 'th' ? 'th' : 'en');
  // Reload so every JS-rendered string re-renders in the new language.
  location.reload();
}

/** Translate a key with {placeholders}. Falls back to English, then the key. */
function t(key, vars) {
  const lang = getLang();
  let s = (I18N[lang] && I18N[lang][key]) || I18N.en[key] || key;
  if (vars) for (const k of Object.keys(vars)) s = s.split('{' + k + '}').join(vars[k]);
  return s;
}

/** Localised difficulty label (easy/medium/hard → EASY / ง่าย, etc.). */
function tDiff(d) { return t('diff.' + String(d || '').toLowerCase()); }

/** Format an ISO datetime in the active locale (used for scheduled-open times). */
function fmtWhen(iso) {
  if (!iso) return '';
  const d = new Date(iso);
  return d.toLocaleString(getLang() === 'th' ? 'th-TH' : undefined, { dateStyle: 'medium', timeStyle: 'short' });
}

/** Translate static HTML annotated with data-i18n attributes. */
function applyI18n(root) {
  root = root || document;
  root.querySelectorAll('[data-i18n]').forEach((el) => { el.innerHTML = t(el.dataset.i18n); });
  root.querySelectorAll('[data-i18n-ph]').forEach((el) => { el.placeholder = t(el.dataset.i18nPh); });
  root.querySelectorAll('[data-i18n-aria]').forEach((el) => { el.setAttribute('aria-label', t(el.dataset.i18nAria)); });
  root.querySelectorAll('[data-i18n-title]').forEach((el) => { el.title = t(el.dataset.i18nTitle); });
}

/** Accessible EN | ไทย segmented switch. Appended to `host` (or auto-found). */
function mountLangSwitch(host) {
  const lang = getLang();
  const wrap = document.createElement('div');
  wrap.className = 'lang-switch';
  wrap.setAttribute('role', 'group');
  wrap.setAttribute('aria-label', t('common.langSwitch'));
  wrap.innerHTML = `
    <button type="button" lang="en" aria-pressed="${lang === 'en'}" class="${lang === 'en' ? 'on' : ''}">EN</button>
    <button type="button" lang="th" aria-pressed="${lang === 'th'}" class="${lang === 'th' ? 'on' : ''}">ไทย</button>`;
  wrap.querySelectorAll('button').forEach((b) => {
    b.addEventListener('click', () => { if (b.getAttribute('lang') !== lang) setLang(b.getAttribute('lang')); });
  });
  host = host || document.querySelector('.nav-actions') || document.body;
  host.appendChild(wrap);
  return wrap;
}

